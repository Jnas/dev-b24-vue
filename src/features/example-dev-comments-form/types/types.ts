/**
 * Представляет данные пользователя в системе
 */
export type UserType = {
    /** Уникальный идентификатор пользователя */
    ID: string;
    /** Имя пользователя */
    NAME: string;
    /** Фамилия пользователя */
    LAST_NAME: string;
    /** Отчество пользователя (может быть пустым) */
    SECOND_NAME: string;
    /** URL аватара пользователя */
    PERSONAL_PHOTO: string;
};

/**
 * Представляет комментарий в формате хранения в CRM
 */
export type CrmCommentType = {
    /** ID пользователя, оставившего комментарий */
    userId: string;
    /** Текст комментария */
    text: string;
    /** Временная метка создания комментария (timestamp в миллисекундах) */
    timestamp: number;
};

/**
 * Представляет комментарий в формате приложения
 * (обогащенная версия CrmCommentType с дополнительными данными для отображения)
 */
export type CommentType = {
    /** ID пользователя */
    userId: string;
    /** Полное имя пользователя (ФИО) */
    username: string;
    /** URL аватара пользователя */
    avatar: string;
    /** Текст комментария */
    text: string;
    /** Временная метка создания комментария */
    timestamp: number;
};
